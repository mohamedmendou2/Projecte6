# Descripció: Aquest mòdul conté funcions per convertir entre diferents unitats de temps.
# Instruccions de entrada:



def segons_a_hores(segons):
    return segons / 3600

def segons_a_minuts(segons):
    return segons / 60

def hores_a_dies(hores):
    return hores / 24


