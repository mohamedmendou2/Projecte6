# Descripció: Troba el numero maxim d'una llista de numeros

estrelles = ['*', '**', '***', '****', "*****"]

# Inicialitza la variable 'i' a 0 per utilitzar-la com a índex
i = 0 

# Utilitza un bucle 'while' per iterar sobre la llista 'estrelles'
while i < len(estrelles):
    # Mostra l'element actual de la llista
    print(estrelles[i])
    # Incrementa l'índex 'i' en 1 per passar a l'element següent
    i += 1