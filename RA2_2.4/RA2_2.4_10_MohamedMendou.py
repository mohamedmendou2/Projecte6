# Descripció: Programa que contatena dues cadenes separades per un guio -
# Instruccions de entrada: Introduir una cadena de text

# Demana a l'usuari que introdueixi la primera cadena de text
cadena1 = input("Introdueix la primera cadena de text: ")

# Demana a l'usuari que introdueixi la segona cadena de text
cadena2 = input("Introdueix la segona cadena de text: ")

# Concatena les dues cadenes amb un guió enmig
cadena_concatenada = cadena1 + "-" + cadena2
# Mostra la cadena concatenada
print("Cadena concatenada:", cadena_concatenada)