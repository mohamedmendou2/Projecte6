# Descripció: Demana una cadena i mostra la primera i l'ultima lletra
# Instruccions de entrada: Introduir una cadena de text

# Demana a l'usuari que introdueixi una cadena de text
cadena = input("Introdueix una cadena de text: ")
# Obté la primera i l'última lletra de la cadena
primera_lletra = cadena[0]
ultima_lletra = cadena[-1]
# Mostra la primera i l'última lletra
print(f"La primera lletra es: {primera_lletra}")
print(f"L'ultima lletra es: {ultima_lletra}")
